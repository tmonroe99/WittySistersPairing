
public class ListNode {
	
	Girl data;
	ListNode next;
	
	public ListNode(Girl data, ListNode next) {
		this.data = data;
		this.next = next;
	}

	public Girl getData() {
		return data;
	}

	public void setData(Girl data) {
		this.data = data;
	}

	public ListNode getNext() {
		return next;
	}

	public void setNext(ListNode next) {
		this.next = next;
	}
	
	

}
